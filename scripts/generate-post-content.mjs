import fs from "node:fs";
import path from "node:path";
import matter from "gray-matter";
import MarkdownIt from "markdown-it";

const POSTS_DIR = path.resolve("./posts");
const OUTPUT_DIR = path.resolve("./src/app/content/generated");
const OUTPUT_FILE = path.join(OUTPUT_DIR, "posts.generated.ts");

const md = new MarkdownIt({
  html: true,
  linkify: true,
  typographer: true,
});

const REQUIRED_FIELDS = [
  "title",
  "description",
  "published",
  "datePublished",
  "picture",
];

function assertRequiredFields(data, file) {
  for (const field of REQUIRED_FIELDS) {
    if (!(field in data)) {
      throw new Error(
        `${file}: missing required front matter field "${field}"`,
      );
    }
  }
}

function assertValidPost(data, content, file, id) {
  assertRequiredFields(data, file);

  if (typeof data.title !== "string" || !data.title.trim()) {
    throw new Error(`${file}: title must be a non-empty string`);
  }
  if (typeof data.description !== "string" || !data.description.trim()) {
    throw new Error(`${file}: description must be a non-empty string`);
  }
  if (typeof data.published !== "boolean") {
    throw new Error(`${file}: published must be a boolean`);
  }
  if (typeof data.datePublished !== "string" || !data.datePublished.trim()) {
    throw new Error(`${file}: datePublished must be a non-empty string`);
  }
  if (typeof data.picture !== "string" || !data.picture.trim()) {
    throw new Error(`${file}: picture must be a non-empty string`);
  }
  if (!content.trim()) {
    throw new Error(`${file}: markdown body is empty`);
  }
  if (!id.trim()) {
    throw new Error(`${file}: invalid empty filename stem`);
  }

  const parsedDate = new Date(data.datePublished);
  if (Number.isNaN(parsedDate.getTime())) {
    throw new Error(`${file}: invalid datePublished "${data.datePublished}"`);
  }

  const lower = content.toLowerCase();
  if (lower.includes("<script")) {
    throw new Error(`${file}: disallowed <script tag detected`);
  }
}

function toTsString(value) {
  return JSON.stringify(value);
}

const files = fs
  .readdirSync(POSTS_DIR)
  .filter((f) => f.endsWith(".md"))
  .sort();

if (files.length === 0) {
  throw new Error("No markdown files found in ./posts");
}

const seenIds = new Set();
const publishedPosts = [];

for (const file of files) {
  const id = path.basename(file, ".md");

  if (seenIds.has(id)) {
    throw new Error(`Duplicate post id detected: "${id}"`);
  }
  seenIds.add(id);

  const fullPath = path.join(POSTS_DIR, file);
  const raw = fs.readFileSync(fullPath, "utf-8");
  const { data, content } = matter(raw);

  assertValidPost(data, content, file, id);

  if (!data.published) {
    continue;
  }

  const html = md.render(content);
  const datePublishedIso = new Date(data.datePublished).toISOString();
  const route = `/posts/${id}`;

  publishedPosts.push({
    id,
    route,
    sourceFile: file,
    title: data.title,
    description: data.description,
    published: data.published,
    datePublished: data.datePublished,
    datePublishedIso,
    picture: data.picture,
    html,
  });
}

publishedPosts.sort((a, b) =>
  b.datePublishedIso.localeCompare(a.datePublishedIso),
);

const manifest = publishedPosts.map(({ html, ...summary }) => summary);
const byId = Object.fromEntries(publishedPosts.map((post) => [post.id, post]));
const ids = publishedPosts.map((post) => post.id);
const routes = publishedPosts.map((post) => post.route);

const fileContents = `/* AUTO-GENERATED FILE. DO NOT EDIT DIRECTLY. */
/* Generated by scripts/generate-post-content.mjs */

import type { PostSummary, PostDocument } from '../content.models';

export const POST_MANIFEST: readonly PostSummary[] = ${toTsString(manifest)} as const;

export const POSTS_BY_ID: Readonly<Record<string, PostDocument>> = ${toTsString(byId)} as const;

export const POST_IDS: readonly string[] = ${toTsString(ids)} as const;

export const POST_ROUTES: readonly \`/posts/\${string}\`[] = ${toTsString(routes)} as const;
`;

fs.mkdirSync(OUTPUT_DIR, { recursive: true });
fs.writeFileSync(OUTPUT_FILE, fileContents, "utf-8");

console.log(
  `Generated ${publishedPosts.length} published posts -> ${OUTPUT_FILE}`,
);

// === ALSO GENERATE PRERENDER ROUTES ===

const prerenderRoutes = [
  "/blog",
  "/contact",
  "/about",
  "/resume",
  ...ids.map((id) => `/posts/${id}`),
];

const routesFilePath = path.resolve("./prerender-routes.txt");

fs.writeFileSync(routesFilePath, prerenderRoutes.join("\n") + "\n", "utf-8");

console.log(`Generated prerender routes -> ${routesFilePath}`);
